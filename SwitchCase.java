public class SwitchCase {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int bulan = 8;
        int tahun = 2000;
        Bulan b = new Bulan();
        System.out.print("Sekarang bulan \t: ");
        System.out.println(b.cekBulan(bulan));
        System.out.print("Jumlah hari \t: ");
        System.out.println(b.cekJumlahHari(tahun, bulan));
    }
    
}

public class Bulan {

    String cekBulan(int month) {
        String monthString;
        switch (month) {
            case 1:
                monthString = "January";
                break;
            case 2:
                monthString = "February";
                break;
            case 3:
                monthString = "March";
                break;
            case 4:
                monthString = "April";
                break;
            case 5:
                monthString = "May";
                break;
            case 6:
                monthString = "June";
                break;
            case 7:
                monthString = "July";
                break;
            case 8:
                monthString = "August";
                break;
            case 9:
                monthString = "September";
                break;
            case 10:
                monthString = "October";
                break;
            case 11:
                monthString = "November";
                break;
            case 12:
                monthString = "December";
                break;
            default:
                monthString = "Invalid month";
                break;
        }
        return monthString;
    }
    Integer cekJumlahHari(int year, int month){
        int numDays = 0;

        switch (month) {
            case 1: case 3: case 5:
            case 7: case 8: case 10:
            case 12:
                numDays = 31;
                break;
            case 4: case 6:
            case 9: case 11:
                numDays = 30;
                break;
            case 2:
                if (((year % 4 == 0) && 
                     !(year % 100 == 0))
                     || (year % 400 == 0))
                    numDays = 29;
                else
                    numDays = 28;
                break;
            default:
                System.out.println("Invalid month.");
                break;
        }
        return numDays;
    }

}
